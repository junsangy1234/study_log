package chap_03;

public class _02_For {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++){
            System.out.println("Hello, Here's Naco" + i);
        }

        //fori Enter
        for (int i = 2; i <= 20 ; i += 2) {
            System.out.println(i);
        }

    }
}
